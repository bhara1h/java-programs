public class MathOperations {
    public static void main(String[] args) 
{
        double power = Math.pow(5, 6);
        System.out.println("Power(5, 6) = " + power);
        double sqrt = Math.sqrt(45);
        System.out.println("Sqrt(45) = " + sqrt);
        double log = Math.log(1);
        System.out.println("Log(1) = " + log);
    }
}